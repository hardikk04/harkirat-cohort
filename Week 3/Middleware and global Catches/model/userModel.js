const mongoose = require("mongoose");

const userScehma = mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
});

const userModel = mongoose.model("user", userScehma);

module.exports = userModel;
